public class DataTypes {
    public static void main(String[] args) {
        byte a = 124;
        short b = 789;
        int c = 56327;
        long d = 123456789;
        float e = 7.89f;
        char f = 'y';

        System.out.println(a);
        System.out.printf("short = %d \n int = %d \n long = %d \n float = %f \n char = %c", b, c, d, e, f);
    }
}
